#Escribe un programa que solicite al usuario dos números y luego imprima la suma, 
#la resta, la multiplicación y la división de los dos números

num1 = float(input("Por favor ingrese el valor del primer numero:"))
num2 = float(input("Por favor ingrese el valor del segundo numero:"))

suma = float(num1+num2)
resta = float(num1-num2)
multiplicacion = float(num1*num2)
div = float(num1/num2)

print("El resultado de la suma es:",suma)
print("El resultado de la resta es:",resta)
print("El resultado de la multiplicacion es:",multiplicacion)
print("El resultado de la division es:",div)




